
public class Heure {
	private int heure, minute;

	public void setHeure(int heure) {
		this.heure = heure;
	}

	public void setMinute(int minute) {
		this.minute = minute;
	}

	public int getHeure() {
		return heure;
	}

	public int getMinute() {
		return minute;
	}

	public Heure(int heure, int minute) {
		super();
		this.heure = heure;
		this.minute = minute;
	}
	
}
